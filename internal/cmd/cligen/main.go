// Command cligen generates cobra commands for every Flashduty OpenAPI operation
// not already owned by a hand-curated command, so the CLI covers the full API
// surface and every command's --help is sourced from the API description.
//
// It mirrors go-flashduty's own generator: it reads the SAME vendored spec (from
// the linked go-flashduty module) and reuses naming.go so each generated command
// targets the exact typed SDK method. Structure (method name, request type, data
// return) is read by REFLECTING the compiled SDK — authoritative and always in
// sync; help text (summaries, field descriptions, examples) is read from the
// spec. Run: `go run ./internal/cmd/cligen` from the repo root.
package main

import (
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"os/exec"
	"path/filepath"
	"reflect"
	"sort"
	"strings"

	flashduty "github.com/flashcatcloud/go-flashduty"
)

const genHeader = "// Code generated by internal/cmd/cligen; DO NOT EDIT.\n\n"

// curatedOps lists operationIds already owned by a hand-curated command. The
// generator skips them so curated ergonomics win. Populated in P2.
var curatedOps = map[string]bool{}

func main() {
	only := ""
	for i, a := range os.Args[1:] {
		if a == "--only" && i+1 < len(os.Args[1:]) {
			only = os.Args[1:][i+1]
		}
	}
	if err := run(only); err != nil {
		fmt.Fprintln(os.Stderr, "cligen:", err)
		os.Exit(1)
	}
}

func run(only string) error {
	specPath, err := specPath()
	if err != nil {
		return err
	}
	spec, err := loadSpec(specPath)
	if err != nil {
		return err
	}
	sdk := reflectSDK()

	services := collectServices(asMap(spec["paths"]), asMap(asMap(spec["components"])["schemas"]))

	root, err := repoRoot()
	if err != nil {
		return err
	}
	genDir := filepath.Join(root, "internal", "cli")

	var registered []string
	emitted := 0
	for _, s := range services {
		if only != "" && !strings.Contains(s.Tag, only) {
			continue
		}
		src, n := emitService(s, sdk)
		if n == 0 {
			continue
		}
		if err := writeGo(filepath.Join(genDir, "zz_generated_"+fileName(s.Name)+".go"), src); err != nil {
			return err
		}
		registered = append(registered, s.Name)
		emitted += n
	}

	if only == "" || len(registered) > 0 {
		if err := writeGo(filepath.Join(genDir, "zz_generated_register.go"), emitRegister(registered, only)); err != nil {
			return err
		}
	}
	fmt.Printf("cligen: %d services, %d generated commands\n", len(registered), emitted)
	return nil
}

// ---- spec model ------------------------------------------------------------

type service struct {
	Name string // Go service field on Client, e.g. "Calendars"
	Tag  string
	Ops  []specOp
}

type specOp struct {
	OpID    string
	Method  string // Go SDK method name
	HTTP    string
	Path    string
	Summary string
	Desc    string
	Example string // compact request-body example JSON, "" if none
	Fields  []specField
}

type specField struct {
	Wire     string
	Required bool
	Desc     string
	Enum     []string
}

func collectServices(paths, schemas map[string]any) []service {
	walker := &specWalker{schemas: schemas}
	byTag := map[string][]struct {
		path, http string
		op         map[string]any
	}{}
	for p, item := range paths {
		for method, raw := range asMap(item) {
			m := strings.ToUpper(method)
			if m != "POST" && m != "GET" {
				continue
			}
			o := asMap(raw)
			tags := asSlice(o["tags"])
			if len(tags) == 0 {
				continue
			}
			tag, _ := tags[0].(string)
			byTag[tag] = append(byTag[tag], struct {
				path, http string
				op         map[string]any
			}{p, m, o})
		}
	}

	var tagNames []string
	for t := range byTag {
		tagNames = append(tagNames, t)
	}
	sort.Strings(tagNames)

	var out []service
	for _, tag := range tagNames {
		entries := byTag[tag]
		sort.Slice(entries, func(i, j int) bool {
			return str(entries[i].op, "operationId") < str(entries[j].op, "operationId")
		})
		var opIDs []string
		for _, e := range entries {
			opIDs = append(opIDs, str(e.op, "operationId"))
		}
		names := methodNames(opIDs)
		svc := service{Name: serviceName(tag), Tag: tag}
		for _, e := range entries {
			opID := str(e.op, "operationId")
			if curatedOps[opID] {
				continue
			}
			svc.Ops = append(svc.Ops, specOp{
				OpID:    opID,
				Method:  names[opID],
				HTTP:    e.http,
				Path:    e.path,
				Summary: str(e.op, "summary"),
				Desc:    str(e.op, "description"),
				Example: walker.example(e.op),
				Fields:  walker.fields(e.op),
			})
		}
		if len(svc.Ops) > 0 {
			out = append(out, svc)
		}
	}
	return out
}

type specWalker struct{ schemas map[string]any }

func (w *specWalker) deref(s map[string]any) map[string]any {
	if ref := refName(s); ref != "" {
		return asMap(w.schemas[ref])
	}
	return s
}

// merged returns the flattened properties + required set of a schema, resolving
// $ref and allOf.
func (w *specWalker) merged(s map[string]any) (map[string]any, map[string]bool) {
	props := map[string]any{}
	req := map[string]bool{}
	s = w.deref(s)
	for _, part := range asSlice(s["allOf"]) {
		p, r := w.merged(asMap(part))
		for k, v := range p {
			props[k] = v
		}
		for k := range r {
			req[k] = true
		}
	}
	for k, v := range asMap(s["properties"]) {
		props[k] = v
	}
	for _, r := range asSlice(s["required"]) {
		if name, ok := r.(string); ok {
			req[name] = true
		}
	}
	return props, req
}

func (w *specWalker) fields(op map[string]any) []specField {
	var fields []specField
	if rb := asMap(op["requestBody"]); rb != nil {
		sch := asMap(asMap(asMap(rb["content"])["application/json"])["schema"])
		props, req := w.merged(sch)
		for wire, v := range props {
			pv := w.deref(asMap(v))
			fields = append(fields, specField{
				Wire:     wire,
				Required: req[wire],
				Desc:     str(pv, "description"),
				Enum:     enumStrings(pv),
			})
		}
	}
	for _, raw := range asSlice(op["parameters"]) {
		pm := asMap(raw)
		if str(pm, "in") != "query" {
			continue
		}
		fields = append(fields, specField{
			Wire:     str(pm, "name"),
			Required: boolOf(pm["required"]),
			Desc:     str(pm, "description"),
			Enum:     enumStrings(w.deref(asMap(pm["schema"]))),
		})
	}
	sort.Slice(fields, func(i, j int) bool { return fields[i].Wire < fields[j].Wire })
	return fields
}

func (w *specWalker) example(op map[string]any) string {
	rb := asMap(op["requestBody"])
	if rb == nil {
		return ""
	}
	ex, ok := asMap(asMap(rb["content"])["application/json"])["example"]
	if !ok {
		return ""
	}
	b, err := json.Marshal(ex)
	if err != nil {
		return ""
	}
	return string(b)
}

// ---- reflection over the compiled SDK -------------------------------------

type methodInfo struct {
	ReqType string // request struct name ("" => no body param)
	HasData bool   // method returns (*Data, *Response, error)
}

// eachServiceField walks a Client value, descending into its embedded
// genServices struct, and invokes fn for every *XxxService handle with its
// field name (the service name, e.g. "Calendars").
func eachServiceField(cv reflect.Value, fn func(name string, svcType reflect.Type)) {
	ct := cv.Type()
	for i := 0; i < ct.NumField(); i++ {
		f := ct.Field(i)
		if f.Anonymous && deref(f.Type).Kind() == reflect.Struct {
			fv := cv.Field(i)
			if fv.Kind() == reflect.Ptr {
				fv = fv.Elem()
			}
			if fv.IsValid() {
				eachServiceField(fv, fn)
			}
			continue
		}
		if f.Type.Kind() == reflect.Ptr && strings.HasSuffix(f.Type.Elem().Name(), "Service") {
			fn(f.Name, f.Type)
		}
	}
}

// reflectSDK introspects a real Client so the generator targets the exact
// method/request-type names the SDK exposes, regardless of how the spec maps to
// Go identifiers.
func reflectSDK() map[string]map[string]methodInfo {
	client, err := flashduty.NewClient("cligen-dummy-key")
	if err != nil {
		panic("cligen: NewClient: " + err.Error())
	}
	out := map[string]map[string]methodInfo{}
	eachServiceField(reflect.ValueOf(client).Elem(), func(name string, svcType reflect.Type) {
		methods := map[string]methodInfo{}
		for m := 0; m < svcType.NumMethod(); m++ {
			meth := svcType.Method(m)
			mt := meth.Func.Type() // In(0)=receiver, In(1)=ctx, In(2)=*Req?
			info := methodInfo{HasData: mt.NumOut() == 3}
			if mt.NumIn() >= 3 {
				rt := mt.In(2)
				if rt.Kind() == reflect.Ptr {
					rt = rt.Elem()
				}
				info.ReqType = rt.Name()
			}
			methods[meth.Name] = info
		}
		out[name] = methods
	})
	return out
}

// reqFields reflects a request struct's flag-able scalar fields (flattening
// embedded structs) and the names of complex fields (reachable only via --data).
func reqFields(reqType string) (scalars []scalarField, complex []string) {
	if reqType == "" {
		return nil, nil
	}
	t := typeByName(reqType)
	if t == nil {
		return nil, nil
	}
	var walk func(t reflect.Type)
	seen := map[string]bool{}
	walk = func(t reflect.Type) {
		for i := 0; i < t.NumField(); i++ {
			f := t.Field(i)
			if f.Anonymous && deref(f.Type).Kind() == reflect.Struct {
				walk(deref(f.Type))
				continue
			}
			wire := strings.Split(f.Tag.Get("json"), ",")[0]
			if wire == "" || wire == "-" || seen[wire] {
				continue
			}
			seen[wire] = true
			kind, ok := scalarKind(f.Type)
			if !ok {
				complex = append(complex, wire)
				continue
			}
			scalars = append(scalars, scalarField{Wire: wire, Kind: kind})
		}
	}
	walk(t)
	return scalars, complex
}

type scalarField struct {
	Wire string
	Kind string // "string","bool","int","float","[]string","[]int"
}

var typeRegistry = buildTypeRegistry()

func buildTypeRegistry() map[string]reflect.Type {
	reg := map[string]reflect.Type{}
	client, err := flashduty.NewClient("cligen-dummy-key")
	if err != nil {
		panic("cligen: NewClient: " + err.Error())
	}
	eachServiceField(reflect.ValueOf(client).Elem(), func(_ string, svcType reflect.Type) {
		for m := 0; m < svcType.NumMethod(); m++ {
			mt := svcType.Method(m).Func.Type()
			if mt.NumIn() >= 3 {
				rt := mt.In(2)
				if rt.Kind() == reflect.Ptr {
					rt = rt.Elem()
				}
				reg[rt.Name()] = rt
			}
		}
	})
	return reg
}

func typeByName(name string) reflect.Type { return typeRegistry[name] }

func deref(t reflect.Type) reflect.Type {
	for t.Kind() == reflect.Ptr {
		t = t.Elem()
	}
	return t
}

func scalarKind(t reflect.Type) (string, bool) {
	t = deref(t)
	switch t.Kind() {
	case reflect.String:
		return "string", true
	case reflect.Bool:
		return "bool", true
	case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64,
		reflect.Uint, reflect.Uint8, reflect.Uint16, reflect.Uint32, reflect.Uint64:
		return "int", true
	case reflect.Float32, reflect.Float64:
		return "float", true
	case reflect.Slice:
		switch deref(t.Elem()).Kind() {
		case reflect.String:
			return "[]string", true
		case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64,
			reflect.Uint, reflect.Uint8, reflect.Uint16, reflect.Uint32, reflect.Uint64:
			return "[]int", true
		}
	}
	return "", false
}

// ---- emission --------------------------------------------------------------

func emitService(s service, sdk map[string]map[string]methodInfo) (string, int) {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("package cli\n\n")
	b.WriteString("import (\n\t\"github.com/spf13/cobra\"\n\n\tflashduty \"github.com/flashcatcloud/go-flashduty\"\n)\n\n")

	methods := sdk[s.Name]
	var register strings.Builder
	fmt.Fprintf(&register, "func registerGenerated%s(root *cobra.Command) {\n", s.Name)

	n := 0
	groups := map[string]string{} // group-path key -> Go var expr
	for _, o := range s.Ops {
		mi, ok := methods[o.Method]
		if !ok {
			// Method not found by reflection (naming drift); skip rather than emit broken code.
			fmt.Fprintf(os.Stderr, "cligen: WARN %s.%s (%s) not found on SDK; skipped\n", s.Name, o.Method, o.OpID)
			continue
		}
		fn := "gen" + s.Name + o.Method + "Cmd"
		b.WriteString(emitCmd(fn, s, o, mi))
		// registration: build the group chain from the path, attach the leaf.
		segs := pathSegs(o.Path)
		parent := "root"
		key := ""
		for _, g := range segs[:len(segs)-1] {
			key += "/" + g
			if v, done := groups[key]; done {
				parent = v
			} else {
				varName := "g" + sanitize(key)
				fmt.Fprintf(&register, "\t%s := genGroup(%s, %q, %q)\n", varName, parent, kebab(g), s.Tag+" API")
				groups[key] = varName
				parent = varName
			}
		}
		fmt.Fprintf(&register, "\tgenAddLeaf(%s, %s())\n", parent, fn)
		n++
	}
	register.WriteString("}\n")
	b.WriteString(register.String())
	return b.String(), n
}

func emitCmd(fn string, s service, o specOp, mi methodInfo) string {
	scalars, complexFields := reqFields(mi.ReqType)
	// Spec metadata by wire for help + required annotation.
	specByWire := map[string]specField{}
	for _, f := range o.Fields {
		specByWire[f.Wire] = f
	}

	var b strings.Builder
	fmt.Fprintf(&b, "func %s() *cobra.Command {\n", fn)
	b.WriteString("\tvar dataJSON string\n")
	for _, sf := range scalars {
		fmt.Fprintf(&b, "\tvar %s %s\n", flagVar(sf.Wire), goFlagType(sf.Kind))
	}

	// Command literal.
	verb := kebab(pathSegs(o.Path)[len(pathSegs(o.Path))-1])
	fmt.Fprintf(&b, "\tcmd := &cobra.Command{\n")
	fmt.Fprintf(&b, "\t\tUse:   %q,\n", verb)
	fmt.Fprintf(&b, "\t\tShort: %q,\n", oneLine(o.Summary))
	fmt.Fprintf(&b, "\t\tLong:  %s,\n", quoteMultiline(longHelp(o, scalars, complexFields, specByWire)))
	if ex := exampleHelp(o, verb, s); ex != "" {
		fmt.Fprintf(&b, "\t\tExample: %s,\n", quoteMultiline(ex))
	}
	b.WriteString("\t\tRunE: func(cmd *cobra.Command, args []string) error {\n")
	b.WriteString("\t\t\treturn runCommand(cmd, args, func(ctx *RunContext) error {\n")
	// body assembly
	b.WriteString("\t\t\t\tbody, err := genAssembleBody(dataJSON, func(body map[string]any) {\n")
	for _, sf := range scalars {
		fmt.Fprintf(&b, "\t\t\t\t\tif cmd.Flags().Changed(%q) {\n\t\t\t\t\t\tbody[%q] = %s\n\t\t\t\t\t}\n",
			flagName(sf.Wire), sf.Wire, flagVar(sf.Wire))
	}
	b.WriteString("\t\t\t\t})\n")
	b.WriteString("\t\t\t\tif err != nil {\n\t\t\t\t\treturn err\n\t\t\t\t}\n")

	call := fmt.Sprintf("ctx.Client.%s.%s(cmdContext(ctx.Cmd)", s.Name, o.Method)
	if mi.ReqType != "" {
		fmt.Fprintf(&b, "\t\t\t\treq := new(flashduty.%s)\n", mi.ReqType)
		b.WriteString("\t\t\t\tif err := genBindBody(body, req); err != nil {\n\t\t\t\t\treturn err\n\t\t\t\t}\n")
		call += ", req)"
	} else {
		b.WriteString("\t\t\t\t_ = body\n")
		call += ")"
	}

	switch {
	case mi.HasData:
		fmt.Fprintf(&b, "\t\t\t\tout, _, err := %s\n", call)
		b.WriteString("\t\t\t\tif err != nil {\n\t\t\t\t\treturn err\n\t\t\t\t}\n")
		b.WriteString("\t\t\t\treturn printGenericResult(ctx, out)\n")
	default:
		fmt.Fprintf(&b, "\t\t\t\t_, err = %s\n", call)
		b.WriteString("\t\t\t\tif err != nil {\n\t\t\t\t\treturn err\n\t\t\t\t}\n")
		fmt.Fprintf(&b, "\t\t\t\tctx.WriteResult(%q)\n", "OK: "+o.HTTP+" "+o.Path)
		b.WriteString("\t\t\t\treturn nil\n")
	}
	b.WriteString("\t\t\t})\n\t\t},\n\t}\n")

	// flags
	for _, sf := range scalars {
		usage := flagUsage(specByWire[sf.Wire])
		fmt.Fprintf(&b, "\tcmd.Flags().%s(&%s, %q, %s, %q)\n",
			flagSetter(sf.Kind), flagVar(sf.Wire), flagName(sf.Wire), flagZero(sf.Kind), usage)
	}
	b.WriteString("\tcmd.Flags().StringVar(&dataJSON, \"data\", \"\", \"Full request body as JSON; typed flags override its fields\")\n")
	b.WriteString("\treturn cmd\n}\n\n")
	return b.String()
}

func emitRegister(services []string, only string) string {
	var b strings.Builder
	b.WriteString(genHeader)
	b.WriteString("package cli\n\n")
	b.WriteString("import \"github.com/spf13/cobra\"\n\n")
	b.WriteString("// registerGenerated attaches every generated command group to root. Called\n// from root.go init() after curated commands so curated leaves win on conflict.\n")
	b.WriteString("func registerGenerated(root *cobra.Command) {\n")
	for _, s := range services {
		fmt.Fprintf(&b, "\tregisterGenerated%s(root)\n", s)
	}
	b.WriteString("}\n")
	return b.String()
}

// ---- help text -------------------------------------------------------------

func longHelp(o specOp, scalars []scalarField, complexFields []string, byWire map[string]specField) string {
	var b strings.Builder
	if o.Summary != "" {
		b.WriteString(strings.TrimSuffix(o.Summary, ".") + ".\n")
	}
	if o.Desc != "" && o.Desc != o.Summary {
		b.WriteString("\n" + o.Desc + "\n")
	}
	fmt.Fprintf(&b, "\nAPI: %s %s (%s)\n", o.HTTP, o.Path, o.OpID)
	if len(scalars) > 0 || len(complexFields) > 0 {
		b.WriteString("\nRequest fields:\n")
		for _, sf := range scalars {
			f := byWire[sf.Wire]
			req := ""
			if f.Required {
				req = " (required)"
			}
			line := fmt.Sprintf("  --%s %s%s", flagName(sf.Wire), sf.Kind, req)
			if f.Desc != "" {
				line += " — " + oneLine(f.Desc)
			}
			if len(f.Enum) > 0 {
				line += " [" + strings.Join(f.Enum, ", ") + "]"
			}
			b.WriteString(line + "\n")
		}
		for _, wire := range complexFields {
			f := byWire[wire]
			req := ""
			if f.Required {
				req = " (required)"
			}
			line := fmt.Sprintf("  %s (JSON, via --data)%s", wire, req)
			if f.Desc != "" {
				line += " — " + oneLine(f.Desc)
			}
			b.WriteString(line + "\n")
		}
	}
	return b.String()
}

func exampleHelp(o specOp, verb string, s service) string {
	cmdPath := "flashduty " + strings.Join(mapSlice(pathSegs(o.Path), kebab), " ")
	if o.Example != "" {
		return "  " + cmdPath + " --data '" + o.Example + "'"
	}
	return ""
}

func flagUsage(f specField) string {
	u := oneLine(f.Desc)
	if u == "" {
		u = "Request field " + f.Wire
	}
	if f.Required {
		u += " (required)"
	}
	if len(f.Enum) > 0 {
		u += " [" + strings.Join(f.Enum, ", ") + "]"
	}
	return u
}

// ---- small helpers ---------------------------------------------------------

func flagName(wire string) string { return kebab(wire) }
func flagVar(wire string) string  { return "f" + pascal(tokens(wire)) }

func goFlagType(kind string) string {
	switch kind {
	case "string":
		return "string"
	case "bool":
		return "bool"
	case "int":
		return "int64"
	case "float":
		return "float64"
	case "[]string":
		return "[]string"
	case "[]int":
		return "[]int"
	}
	return "string"
}

func flagSetter(kind string) string {
	switch kind {
	case "string":
		return "StringVar"
	case "bool":
		return "BoolVar"
	case "int":
		return "Int64Var"
	case "float":
		return "Float64Var"
	case "[]string":
		return "StringSliceVar"
	case "[]int":
		return "IntSliceVar"
	}
	return "StringVar"
}

func flagZero(kind string) string {
	switch kind {
	case "string":
		return `""`
	case "bool":
		return "false"
	case "int":
		return "0"
	case "float":
		return "0"
	case "[]string":
		return "nil"
	case "[]int":
		return "nil"
	}
	return `""`
}

func pathSegs(p string) []string {
	var out []string
	for _, s := range strings.Split(p, "/") {
		if s != "" {
			out = append(out, s)
		}
	}
	return out
}

func sanitize(s string) string {
	var b strings.Builder
	for _, t := range tokens(s) {
		b.WriteString(pascalToken(t))
	}
	return b.String()
}

func mapSlice(in []string, f func(string) string) []string {
	out := make([]string, len(in))
	for i, s := range in {
		out[i] = f(s)
	}
	return out
}

func oneLine(s string) string {
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "\r", " ")
	return strings.Join(strings.Fields(s), " ")
}

func quoteMultiline(s string) string { return "`" + strings.ReplaceAll(s, "`", "'") + "`" }

func enumStrings(s map[string]any) []string {
	var out []string
	for _, v := range asSlice(s["enum"]) {
		if str, ok := v.(string); ok && str != "" {
			out = append(out, str)
		}
	}
	return out
}

func boolOf(v any) bool { b, _ := v.(bool); return b }

func refName(s map[string]any) string {
	ref, _ := s["$ref"].(string)
	if ref == "" {
		return ""
	}
	return ref[strings.LastIndex(ref, "/")+1:]
}

func asMap(v any) map[string]any { m, _ := v.(map[string]any); return m }
func asSlice(v any) []any        { s, _ := v.([]any); return s }
func str(m map[string]any, k string) string {
	s, _ := m[k].(string)
	return s
}

func fileName(svc string) string {
	var b strings.Builder
	for i, r := range svc {
		if r >= 'A' && r <= 'Z' {
			if i > 0 {
				b.WriteByte('_')
			}
			b.WriteRune(r - 'A' + 'a')
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

func writeGo(path, src string) error {
	formatted, err := format.Source([]byte(src))
	if err != nil {
		_ = os.WriteFile(path, []byte(src), 0o644)
		return fmt.Errorf("format %s: %w", filepath.Base(path), err)
	}
	return os.WriteFile(path, formatted, 0o644)
}

func loadSpec(path string) (map[string]any, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var spec map[string]any
	if err := json.Unmarshal(data, &spec); err != nil {
		return nil, fmt.Errorf("parse spec: %w", err)
	}
	return spec, nil
}

// specPath resolves the openapi.en.json shipped in the linked go-flashduty
// module, so the CLI always generates against the spec its SDK was built from.
func specPath() (string, error) {
	out, err := exec.Command("go", "list", "-m", "-f", "{{.Dir}}", "github.com/flashcatcloud/go-flashduty").Output()
	if err != nil {
		return "", fmt.Errorf("locate go-flashduty module: %w", err)
	}
	dir := strings.TrimSpace(string(out))
	return filepath.Join(dir, "openapi", "openapi.en.json"), nil
}

func repoRoot() (string, error) {
	wd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for dir := wd; ; {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found from %s", wd)
		}
		dir = parent
	}
}
