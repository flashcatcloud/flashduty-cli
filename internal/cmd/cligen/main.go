func pathSegs(p string) []string {
	var out []string
	for _, s := range strings.Split(p, "/") {
		if s != "" {
			out = append(out, s)
		}
	}
	return out
}

// cliGroup is the top-level command group for an operation: the kebab of its
// first path segment. Generated ops merge into the curated group of the same
// name (e.g. /incident/* joins the curated `incident` command).
func cliGroup(path string) string { return kebab(pathSegs(path)[0]) }

// cliVerb is the leaf verb: the remaining path segments after the group,
// hyphen-joined. A single-segment path uses that segment as the verb. Because
// every operation becomes exactly one leaf (never a parent of another op), this
// flat shape is collision-free even for prefix-nested paths.
func cliVerb(path string) string {
	segs := pathSegs(path)
	if len(segs) == 1 {
		return kebab(segs[0])
	}
	return strings.Join(mapSlice(segs[1:], kebab), "-")
}

// computeGroupShorts assigns each top-level group a one-line description. A group
// that holds a single tag's ops uses that tag; a group spanning several tags
// (e.g. `monit` = Alert rules + Data sources + Rule sets) uses their common
// tag-prefix ("Monitors"). Groups that merge into a curated command keep the
// curated short (genGroup reuses the existing command), so this only sets the
// short for generated-only groups.
func computeGroupShorts(services []service) map[string]string {
	tagsByGroup := map[string]map[string]bool{}
	for _, s := range services {
		for _, o := range s.Ops {
			g := cliGroup(o.Path)
			if tagsByGroup[g] == nil {
				tagsByGroup[g] = map[string]bool{}
			}
			tagsByGroup[g][s.Tag] = true
		}
	}
	out := map[string]string{}
	for g, set := range tagsByGroup {
		var tags []string
		for t := range set {
			tags = append(tags, t)
		}
		sort.Strings(tags)
		out[g] = groupShort(tags)
	}
	return out
}

func groupShort(tags []string) string {
	if len(tags) == 0 {
		return "Flashduty API"
	}
	if len(tags) == 1 {
		return tags[0] + " API"
	}
	common := strings.Split(tags[0], "/")
	for _, t := range tags[1:] {
		segs := strings.Split(t, "/")
		i := 0
		for i < len(common) && i < len(segs) && common[i] == segs[i] {
			i++
		}
		common = common[:i]
	}
	if len(common) > 0 {
		return strings.Join(common, "/") + " API"
	}
	return "Flashduty API"
}

func sanitize(s string) string {
	var b strings.Builder
	for _, t := range tokens(s) {
		b.WriteString(pascalToken(t))
	}
	return b.String()
}

func mapSlice(in []string, f func(string) string) []string {
	out := make([]string, len(in))
	for i, s := range in {
		out[i] = f(s)
	}
	return out
}

func oneLine(s string) string {
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "\r", " ")
	return strings.Join(strings.Fields(s), " ")
}

func quoteMultiline(s string) string { return "`" + strings.ReplaceAll(s, "`", "'") + "`" }

func enumStrings(s map[string]any) []string {
	var out []string
	for _, v := range asSlice(s["enum"]) {
		if str, ok := v.(string); ok && str != "" {
			out = append(out, str)
		}
	}
	return out
}

func boolOf(v any) bool { b, _ := v.(bool); return b }

func refName(s map[string]any) string {
	ref, _ := s["$ref"].(string)
	if ref == "" {
		return ""
	}
	return ref[strings.LastIndex(ref, "/")+1:]
}

func asMap(v any) map[string]any { m, _ := v.(map[string]any); return m }
func asSlice(v any) []any        { s, _ := v.([]any); return s }
func str(m map[string]any, k string) string {
	s, _ := m[k].(string)
	return s
}

func fileName(svc string) string {
	var b strings.Builder
	for i, r := range svc {
		if r >= 'A' && r <= 'Z' {
			if i > 0 {
				b.WriteByte('_')
			}
			b.WriteRune(r - 'A' + 'a')
		} else {
			b.WriteRune(r)
		}
	}
	return b.String()
}

func writeGo(path, src string) error {
	formatted, err := format.Source([]byte(src))
	if err != nil {
		_ = os.WriteFile(path, []byte(src), 0o644)
		return fmt.Errorf("format %s: %w", filepath.Base(path), err)
	}
	return os.WriteFile(path, formatted, 0o644)
}

func loadSpec(path string) (map[string]any, error) {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	var spec map[string]any
	if err := json.Unmarshal(data, &spec); err != nil {
		return nil, fmt.Errorf("parse spec: %w", err)
	}
	return spec, nil
}

// specPath resolves the openapi.en.json shipped in the linked go-flashduty
// module, so the CLI always generates against the spec its SDK was built from.
func specPath() (string, error) {
	out, err := exec.Command("go", "list", "-m", "-f", "{{.Dir}}", "github.com/flashcatcloud/go-flashduty").Output()
	if err != nil {
		return "", fmt.Errorf("locate go-flashduty module: %w", err)
	}
	dir := strings.TrimSpace(string(out))
	return filepath.Join(dir, "openapi", "openapi.en.json"), nil
}

func repoRoot() (string, error) {
	wd, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for dir := wd; ; {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found from %s", wd)
		}
		dir = parent
	}
}
